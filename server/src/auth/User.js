class User {
  constructor({
    id,
    username,
    name,
    address,
    city,
    country,
    email,
    phoneNumber
  }) {
    this.id = id;
    this.username = username;
    this.name = name;
    this.address = address;
    this.city = city;
    this.country = country;
    this.email = email;
    this.phoneNumber = phoneNumber;
  }
}
